import { jsPDF } from "jspdf";
import { format } from "date-fns";
import { Employee } from "@/types";

export type DocType = "offer-letter" | "confirmation-letter" | "increment-letter" | "promotion-letter" | "relieving-letter";

export const DOC_TYPE_LABELS: Record<DocType, string> = {
  "offer-letter": "Offer Letter",
  "confirmation-letter": "Confirmation Letter",
  "increment-letter": "Increment Letter",
  "promotion-letter": "Promotion Letter",
  "relieving-letter": "Relieving Letter",
};

function addHeader(doc: jsPDF, pageWidth: number, margin: number) {
  doc.setFillColor(99, 102, 241);
  doc.rect(0, 0, pageWidth, 30, "F");
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(16);
  doc.setFont("helvetica", "bold");
  doc.text("HRMS Enterprise Suite", margin, 18);
  doc.setFontSize(9);
  doc.setFont("helvetica", "normal");
  doc.text("admin@company.com  |  www.hrmsportal.com", margin, 25);
}

function addFooter(doc: jsPDF, pageWidth: number) {
  doc.setFillColor(99, 102, 241);
  doc.rect(0, doc.internal.pageSize.getHeight() - 12, pageWidth, 12, "F");
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(8);
  doc.text("Confidential — This document is generated by HRMS Enterprise Suite", pageWidth / 2, doc.internal.pageSize.getHeight() - 4, { align: "center" });
}

function addSignatures(doc: jsPDF, margin: number, pageWidth: number, y: number) {
  doc.setFont("helvetica", "bold");
  doc.setTextColor(0, 0, 0);
  doc.text("Authorised Signatory", margin, y);
  doc.text("HR Department", pageWidth - margin - 50, y);
  y += 6;
  doc.setFont("helvetica", "normal");
  doc.setTextColor(100, 100, 100);
  doc.text("HRMS Enterprise Suite", margin, y);
  doc.text("Human Resources", pageWidth - margin - 50, y);
}

export function generateOfferLetterPDF(employee: Employee) {
  const doc = new jsPDF();
  const margin = 20;
  const pageWidth = doc.internal.pageSize.getWidth();
  let y = 0;

  addHeader(doc, pageWidth, margin);

  y = 45;
  doc.setTextColor(0, 0, 0);
  doc.setFontSize(18);
  doc.setFont("helvetica", "bold");
  doc.text("OFFER LETTER", pageWidth / 2, y, { align: "center" });

  y += 12;
  doc.setFontSize(10);
  doc.setFont("helvetica", "normal");
  doc.setTextColor(100, 100, 100);
  doc.text(`Date: ${format(new Date(), "dd MMMM yyyy")}`, pageWidth - margin, y, { align: "right" });
  doc.text(`Ref: OL-${employee.employeeId}`, margin, y);

  y += 14;
  doc.setTextColor(0, 0, 0);
  doc.setFontSize(11);
  doc.text(`Dear ${employee.firstName} ${employee.lastName},`, margin, y);

  y += 10;
  doc.setFontSize(10);
  doc.setFont("helvetica", "normal");
  const introText = `We are pleased to offer you the position of ${employee.designation} in our ${employee.department} department. This offer is contingent upon successful completion of our standard background verification process.`;
  const splitIntro = doc.splitTextToSize(introText, pageWidth - margin * 2);
  doc.text(splitIntro, margin, y);
  y += splitIntro.length * 6 + 8;

  const terms = [
    ["Position", employee.designation],
    ["Department", employee.department],
    ["Employment Type", employee.employmentType],
    ["Date of Joining", employee.joiningDate],
    ["Work Location", employee.workLocation || employee.officeLocation],
    ["Reporting Manager", employee.reportingManager],
    ["Shift Timing", employee.shiftTiming],
    ["Company Email", employee.companyEmail],
  ];

  doc.setFillColor(240, 240, 255);
  doc.rect(margin, y - 4, pageWidth - margin * 2, 10, "F");
  doc.setFont("helvetica", "bold");
  doc.setFontSize(11);
  doc.text("Employment Terms & Conditions", margin + 2, y + 3);
  y += 14;

  terms.forEach(([label, value], i) => {
    if (i % 2 === 0) {
      doc.setFillColor(250, 250, 252);
      doc.rect(margin, y - 4, pageWidth - margin * 2, 10, "F");
    }
    doc.setFont("helvetica", "bold");
    doc.setFontSize(9);
    doc.setTextColor(80, 80, 80);
    doc.text(`${label}:`, margin + 2, y + 2);
    doc.setFont("helvetica", "normal");
    doc.setTextColor(0, 0, 0);
    doc.text(String(value || "—"), margin + 55, y + 2);
    y += 10;
  });

  y += 8;
  doc.setFont("helvetica", "normal");
  doc.setFontSize(10);
  doc.setTextColor(0, 0, 0);
  const closing = "Please sign and return a copy of this letter to confirm your acceptance. We look forward to welcoming you to our team.";
  const splitClosing = doc.splitTextToSize(closing, pageWidth - margin * 2);
  doc.text(splitClosing, margin, y);
  y += splitClosing.length * 6 + 20;

  addSignatures(doc, margin, pageWidth, y);
  addFooter(doc, pageWidth);
  return doc;
}

