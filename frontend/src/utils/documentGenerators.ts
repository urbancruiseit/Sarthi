export function generatePDF(employee: Employee, type: DocType) {
  switch (type) {
    case "offer-letter": return generateOfferLetterPDF(employee);
    case "confirmation-letter": return generateConfirmationLetterPDF(employee);
    case "increment-letter": return generateIncrementLetterPDF(employee);
    case "promotion-letter": return generatePromotionLetterPDF(employee);
    case "relieving-letter": return generateRelievingLetterPDF(employee);
  }
}

export function getFilename(employee: Employee, type: DocType) {
  const name = `${employee.firstName}_${employee.lastName}`;
  const labels: Record<DocType, string> = {
    "offer-letter": "Offer_Letter",
    "confirmation-letter": "Confirmation_Letter",
    "increment-letter": "Increment_Letter",
    "promotion-letter": "Promotion_Letter",
    "relieving-letter": "Relieving_Letter",
  };
  return `${labels[type]}_${name}.pdf`;
}
